  Widget build(BuildContext context) {
    return GetMaterialApp(
      title: 'Musico',
      debugShowCheckedModeBanner: false,
      theme: ThemeData(
        textTheme: Theme.of(context).textTheme.apply(
              bodyColor: Colors.white,
              displayColor: Colors.white,
